      <b>Ans.</b> Once your complaint is accepted and recorded as being about the conduct of a police officer or member of police staff, it will be dealt with in one of two ways:
	  <h4>Local resolution</h4>
	  <p>Local resolution is an informal approach to resolving complaints. It allows forces to learn lessons and improve the way they do things. Many people prefer their complaint to be dealt with in this way. The Officer believes that when it is carried out effectively and used appropriately, local resolution of less serious matters has a key part to play in the complaints system and in ensuring public confidence.</p>
	  <p>A local manager will discuss your complaint with you and draw up an action plan covering the issues you have raised. Once the action plan has been completed, you will be written to and a copy of the action plan sent to you.</p>
	  <p>If at the end of the local resolution process you are dissatisfied with the outcome, you have a right of appeal. In most cases your appeal will be dealt with by a senior police officer within the Professional Standards Department, although in some cases the appeal will be dealt with by the officer. You will be advised in writing at the end of the local resolution process who will deal with any appeal you wish to make.</p>
	  <h4>Local investigation</h4>
	  <p>If your complaint is not suitable for local resolution, a local investigation will be carried out by a police investigator. The investigator will usually be assigned from your local police area, however, more serious complaints will be dealt with by an investigator from the Professional Standards Department.</p>
	  <p>You will be informed how your complaint will be investigated, what co-operation is required from you, how a decision will be reached and what action will be taken at the end of the investigation. The type of investigation will depend on the nature and seriousness of your complaint and the likely outcome. An investigation might range from telephone enquiries conducted in a few hours to a more extensive process, perhaps taking a number of months.</p>
	  <p>You can appeal if you are unhappy with the outcome of the police investigation into your complaint. Most appeals will be dealt with again by a senior officer within the Professional Standards Department. However, appeals about more serious matters, and those dealt with by investigators from Professional Standards, will be dealt with by the officer.</p>
